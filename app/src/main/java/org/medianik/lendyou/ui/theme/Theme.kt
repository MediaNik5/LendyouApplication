@Composable
fun ProvideLendyouColors(
    colors: LendyouColors,
    content: @Composable () -> Unit
) {
    val colorPalette = remember {
        // Explicitly creating a new object here so we don't mutate the initial [colors]
        // provided, and overwrite the values set in it.
        colors.copy()
    }
    colorPalette.update(colors)
    CompositionLocalProvider(LocalLendyouColors provides colorPalette, content = content)
}

private val LocalLendyouColors = staticCompositionLocalOf<LendyouColors> {
    error("No LendyouColorPalette provided")
}

/**
 * A Material [Colors] implementation which sets all colors to [emptyColor] to discourage usage of
 * [MaterialTheme.colors] in preference to [LendyouTheme.colors].
 */
fun debugColors(
    darkTheme: Boolean,
    emptyColor: Color = Color.Magenta
) = Colors(
    primary = emptyColor,
    primaryVariant = emptyColor,
    secondary = emptyColor,
    secondaryVariant = emptyColor,
    background = emptyColor,
    surface = emptyColor,
    error = emptyColor,
    onPrimary = emptyColor,
    onSecondary = emptyColor,
    onBackground = emptyColor,
    onSurface = emptyColor,
    onError = emptyColor,
    isLight = !darkTheme
)

